    // ---- effective_disabled ----

    #[test]
    fn effective_disabled_blocklist_merges_global_and_profile() {
        let mut base = config::PatternsConfig::default();
        base.disabled.insert("ipv6".to_string());
        let mut profile_dis = HashSet::new();
        profile_dis.insert("secret".to_string());

        let result = effective_disabled(&base, &profile_dis, None);
        assert!(result.disabled.contains("ipv6"));
        assert!(result.disabled.contains("secret"));
        assert!(!result.disabled.contains("url"));
    }

    #[test]
    fn effective_disabled_allowlist_disables_everything_else() {
        let base = config::PatternsConfig::default();
        let mut allowed: HashSet<String> = HashSet::new();
        allowed.insert("url".to_string());
        allowed.insert("ipv4".to_string());

        let result = effective_disabled(&base, &HashSet::new(), Some(&allowed));
        assert!(!result.disabled.contains("url"), "url should be allowed");
        assert!(!result.disabled.contains("ipv4"), "ipv4 should be allowed");
        // Everything else must be disabled.
        for &tag in ALL_BUILTIN_TAGS {
            if tag != "url" && tag != "ipv4" {
                assert!(result.disabled.contains(tag), "{tag} should be disabled");
            }
        }
    }

    #[test]
    fn effective_disabled_allowlist_ignores_global_disable() {
        // Even if global config disables "url", allowlist wins.
        let mut base = config::PatternsConfig::default();
        base.disabled.insert("url".to_string());
        let mut allowed: HashSet<String> = HashSet::new();
        allowed.insert("url".to_string());

        let result = effective_disabled(&base, &HashSet::new(), Some(&allowed));
        assert!(
            !result.disabled.contains("url"),
            "allowlist should re-enable url"
        );
    }
